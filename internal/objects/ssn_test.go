package objects

import "testing"

func TestNewSSN(t *testing.T) {
	tests := []struct {
		input string
		want  SSN
	}{
		// by the way, I don't know whose SSN this is, if it is anyone's
		{"546814527", SSN("546814527")},
		{"546-81-4527", SSN("546814527")},
		{"546 81 4527", SSN("546814527")},
		{"546.81.4527", SSN("546814527")},
		{"5468", SSN("")},
		{"000814527", SSN("")},
		{"000-81-4527", SSN("")},
		{"000 81 4527", SSN("")},
		{"000.81.4527", SSN("")},
		{"666814527", SSN("")},
		{"666-81-4527", SSN("")},
		{"666 81 4527", SSN("")},
		{"666.81.4527", SSN("")},
		{"900814527", SSN("")},
		{"900-81-4527", SSN("")},
		{"900 81 4527", SSN("")},
		{"900.81.4527", SSN("")},
		{"546004527", SSN("")},
		{"546-00-4527", SSN("")},
		{"546 00 4527", SSN("")},
		{"546.00.4527", SSN("")},
		{"546810000", SSN("")},
		{"546-81-0000", SSN("")},
		{"546 81 0000", SSN("")},
		{"546.81.0000", SSN("")},
		{"123456789", SSN("")},
		{"123-45-6789", SSN("")},
		{"123 45 6789", SSN("")},
		{"123.45.6789", SSN("")},
		{"987654321", SSN("")},
		{"987-65-4321", SSN("")},
		{"987 65 4321", SSN("")},
		{"987.65.4321", SSN("")},
		{"111111111", SSN("")},
		{"111-11-1111", SSN("")},
		{"111 11 1111", SSN("")},
		{"111.11.1111", SSN("")},
	}

	for _, test := range tests {
		t.Run(test.input, func(t *testing.T) {
			if got := NewSSN(test.input); got != test.want {
				t.Errorf("got '%v', want '%v'", got, test.want)
			}
		})
	}
}
